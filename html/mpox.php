            <h1 class="titulo_exame" >MPOX</h1>

            <section class="conteudo">

                <h3>Metodologia:</h3>
                <p>
                    RT-PCR em tempo real.
                </p>    
                <h3>Amostra biológica:</h3>
                <p>
                    Swabs de secreções obtidas das lesões de pele (material vesicular), raspados de lesão de pele, ou fragmento das lesões de pele. Após a coleta, os swabs devem ser colocados em tubo de transporte viral seco, fornecido pelo LACEN-PE.              
                </p>    
                <h3>Volume ideal:</h3>
                <p>
                    Não se aplica.
                </p>
                <h3>Período ideal de coleta:</h3>
                <p>
                    As amostras devem ser coletadas no período de aparecimento das lesões de pele (vesículas).
                </p>
                <h3>Orientações para a coleta de amostra:</h3>
                <p>
                    Para a confirmação laboratorial do diagnóstico da MPX, orienta-se a coleta de amostra de,pelo menos, dois sítios diferentes;<br>
                    O melhor desempenho da qPCR é observado nas amostras obtidas com o swab das lesões em fase aguda, período de maior carga viral nestes locais, preferencialmente das lesões abertas;<br>
                    Os swabs deverão ser estéreis, de nylon, poliester ou Dacron;<br>
                    As amostras devem sercolocadas preferencialmente em tubo estéril seco, sem líquido preservante viral, uma vez que os poxvírus mantêm-se estáveis na ausência de qualquer meio preservante;<br>
                    Nas vesículas, a amostra pode ser obtida pelo swabna superfície ou por meio de punçãocom agulha fina. Nas lesões ulceradas, o raspado deve ser direto da lesão.
                </p>
                <h3>Conservação da amostra até o envio:</h3>
                <p>
                    Tubo de fundo cônico de polipropileno com tampa rosqueada, seco, estéril, fornecido pelo LACEN-PE.Conservar a amostracongelada (ou em freezer a -20ºC) até envioao LACEN-PE.
                </p>
                <h3>Forma de acondicionamento para transporte:</h3>
                <p>
                    Os tubos deverão ser transportados em posição vertical de maneira a evitar vazamentos. A organização dentro da caixa térmica deverá ser feita em estantes de modo que não permita atrito e colisão entre os tubos durante o transporte;<br>
                    O transporte deverá ser realizado em caixas térmicas de paredes rígidas e com trava e utilizando gelo reciclável em quantidade suficiente para manter a temperatura das amostras de 2° a 8°C.
                </p>
                <h3>Formulários requeridos:</h3>
                <p>
                    Obrigatório o cadastro no Gerenciador de Ambiente Laboratorial (GAL) e notificação no SINAN.
                </p>
                <h3>Informações complementares:</h3>
                <p>
                    Utilizar caixas isotérmicas com gelo reciclável sinalizadas com símbolo de risco biológico;<br>
                    Usar suporte (estante), forrada com papel absorvente, para conter derramamento ou vazamento durante o transporte;<br>
                    Entregar as amostras na Recepção de Amostras Biológicas do LACEN-PE, já cadastradas no sistema GAL.
                </p>
