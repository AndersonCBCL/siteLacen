            <h1 class="titulo_exame" >Leishmaniose Visceral Canina - LVC</h1>

            <section class="conteudo">

                <h3>Metodologia:</h3>
                <p>
                    Teste rápido (TR DPP) imunocromatográfico;<br>
                    Ensaio Imunoenzimático - EIE
                </p>    
                <h3>Amostra biológica:</h3>
                <p>
                    Soro.
                </p>    
                <h3>Volume ideal:</h3>
                <p>
                    1 ml.
                </p>
                <h3>Período ideal de coleta:</h3>
                <p>
                    A partir da suspeita clínica ou inquéritos/monitoramentos epidemiológicos.
                </p>
                <h3>Orientações para a coleta de amostra em cães:</h3>
                <p>
                    <b>Coleta de sangue e realização do teste rápido (TR DPP)</b>:<br>
                    Escolher o local da punção (lóbulo da orelha, veia jugular, cefálica, femoral ou safena);<br>
                    Separar e identificar o material a ser utilizado;<br>
                    Realizar a contenção adequada do cão; <br>
                    Sempre que necessário depilar a região da coleta; <br>
                    Realizar a antissepsia local com álcool a 70%;<br>
                    Realizar uma punção na extremidade da orelha utilizando uma lanceta estéril e descartável; <br>
                    Com auxílio de uma alça coletora descartável obter uma gota de sangue para preenchimento do círculo da extremidade da alça, captando 5 microlitros de sangue; <br>
                    Liberar o sangue captado diretamente no suporte do teste; <br>
                    Realizar o procedimento diagnóstico conforme instrução do manual do fabricante do kit teste rápido TR DPP. <br>
                    <b>Coleta de sangue venoso para obtenção de soro</b>:<br>
                    Sempre que necessário, depilar a região da coleta; <br>
                    Realizar antissepsia local com álcool a 70%;<br>
                    Localizar a veia e fazer o garrote, tornando-a proeminente; <br>
                    Realizar a punção numa angulação obliqua de 30°, com o bisel da agulha voltado para cima e introduzir com firmeza a agulha na pele e depois no vaso sanguíneo. Deve se tomar cuidado para não estourar a veia levando a formação de hematomas; <br>
                    Retirar a agulha e pressionar o local com algodão, exercendo pressão no local, em geral, de 1 a 2 minutos, evitando sangramentos; <br>
                    Retirar a agulha com auxílio de uma pinça (descartando-a em recipiente adequado), e em seguida dispensar o sangue cuidadosamente na parede do tubo evitando o rompimento das hemácias; <br>            Aguardar a completa coagulação do sangue a temperatura ambiente (60 minutos); <br>
                    Realizar a centrifugação (3000 rpm por 10 minutos) com os tubos devidamente vedado para evitar risco de contaminação tanto da amostra quanto do técnico; <br>
                    Após a centrifugação, transferir preferencialmente o soro para tubo tipo eppendorf, devidamente identificado.            
                </p>
                <h3>Conservação da amostra até o envio:</h3>
                <p>
                    Acondicionar os tubos de amostras de soro em estante ou bandeja, em seguida armazenar em geladeira (2 a 8°C) para envio no dia seguinte ou manter congelado (-20°C) até o momento do envio ao laboratório.
                </p>
                <h3>Forma de acondicionamento para transporte:</h3>
                <p>
                    Organizar as amostras de soro, devidamente identificadas, em estante /bandeja na posição vertical, em seguida acondicionar em caixa isotérmica contendo refis de gelo reciclável. Não usar gelo comum.<br>
                    As amostras devem estar devidamente cadastradas no GAL ANIMAL e acompanhadas da respectiva ficha do cadastro no Gal.<br>
                    Entregar na Recepção LACEN-PE.
                </p>
                <h3>Formulários requeridos:</h3>
                <p>
                    Espelho do GAL;<br>
                    Quando se tratar de necessidade de confirmação do diagnóstico através do Ensaio imunoenzimático – EIE, enviar a informação do resultado reagente, realizado através do teste de triagem - teste rápido TR DPP.

                </p>
                <h3>Informações complementares:</h3>
                <p>
                    As amostras devem ser encaminhadas ao LACEN PE, cadastradas no Sistema de Ambiente Laboratorial (GAL) Animal;<br>
                    Os resultados são disponibilizados via GAL.
                </p>
