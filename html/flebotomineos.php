                <h3>Metodologia:</h3>
                <p>
                    Identificação taxonômica com a chave dicotômica YOUNG DG & amp; DUNCAN MA.
                </p>    
                <h3>Amostra biológica:</h3>
                <p>
                    Flebotomíneos.
                </p>    
                <h3>Quantidade ideal:</h3>
                <p>
                    A partir de um.
                </p>
                <h3>Período ideal de coleta:</h3>
                <p>
                    Definido pela vigilância dos agravos transmitidos por flebotomíneos.
                </p>
                <h3>Orientações para a coleta de amostra em cães:</h3>
                <p>
                    Utilizar armadilha do tipo CDC, ou outro método, a critério da vigilância dos agravos transmitidos por flebotomíneos.
                </p>
                <h3>Conservação da amostra até o envio:</h3>
                <p>
                    Amostra para o primeiro exame: após as retiradas das armadilhas os flebotomíneos devem ser separados,colocados em tubos com álcool a 70% e enviados 100% da amostras coletadas;<br>
                    Amostras para o controle da qualidade (CQ.): devem ser enviados apenas 10% das amostras dos que foram identificadas pelo municípios ou pelas regionais, os flebotomíneos devem vir em tubos com lactofenol.
                </p>    
                <h3>Formulários requeridos:</h3>
                <p>
                    Planilha de campo e etiqueta da armadilha.
                </p>
                <h3>Informações complementares:</h3>
                <p>
                    Para identificação é necessário realizar um processo de clarificação do flebotomíneos:
                    Três horas no hidróxido de potássio a 10%;<br>
                    Vinte minutos no ácido acético a 10%;<br>
                    Três banhos em água destilada com intervalo de quinze minutos cada;<br>
                    Colocar no lactofenol e esperar 24 horas para poder iniciar a análise.<br>
                    A preparação para a analise deve ser realizada em microscópico estereoscópico (lupa), montagem dos espécimesem lâmina;<br>
                    Observar estruturas morfológicas internas em microscópico óptico nas objetivas de 10x, 20x e 40x.


                </p>
