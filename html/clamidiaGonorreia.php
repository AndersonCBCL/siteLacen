                <h3>Volume ideal:</h3>
                <p>
                    10ml a 50ml de urina.
                </p>
                <h3>Período ideal de coleta:</h3>
                <p>
                    A critério do médico solicitante.
                </p>
                <h3>Orientações para a coleta de amostra:</h3>
                <p>
                    Urina: A coleta das amostras devem ser realizadas usando os kits do cobas® PCR Media. Para amostra de urina: deve-se coletar a primeira urina matinal ou a urina retida pela pessoa por, pelo menos, uma hora antes da coleta. Utilizar 10mL a 50mL do primeiro jato urinário. Para melhores resultados, as pessoas com vagina não devem lavar a área genital antes da coleta. As amostras de urina devem ser transferidas para o tubo de cobas® PCR Media.<br>
                    Swab vaginal: Lubrificantes vaginais, géis espermicidas e cremes poderão interferir no desempenho do teste e não deverão ser utilizados antes ou durante a coleta da amostra. Se a amostra coletada contiver sangue em excesso (amostra de cor vermelha ou castanha), não poderá ser utilizada em testes, devendo ser eliminada.
                </p>
                <h3>Conservação da amostra até o envio:</h3>
                <p>
                    Em tubos coletores do kit cobas® de 2ºC a 30ºC.
                </p>
                <h3>Forma de acondicionamento para transporte:</h3>
                <p>
                    Após a coleta da amostra, os tubos devem ser transportados e armazenados em posição vertical entre 2°C e 30°C. As amostras de esfregaços endocervicais, de esfregaços vaginais, de swab orofaríngeo e de swab anorretal coletadas com o cobas® PCR Media Dual Swab Sample Kit, bem como as amostras de urina coletadas com cobas® PCR Urine Sample Kit, podem ser armazenadas entre 2°C e 30°C por até 12 meses após terem sido acondicionadas no cobas® PCR Media.
                </p>
                <h3>Formulários requeridos:</h3>
                <p>
                    As amostras enviadas ao LACEN-PE devem vir acompanhadas do Formulário para Solicitação do Exame Pesquisa de Multipatógenos IST - Detecção Clamídia e Gonococo por Biologia Molecular, devidamente preenchida.
                </p>
                <h3>Informações Complementares:</h3>
