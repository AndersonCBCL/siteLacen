                <h3>Metodologia:</h3>
                <p>
                    Biologia Molecular: PCR em tempo real.
                </p>   
                <h3>Amostra biológica:</h3>
                <p>
                    Urina;<br>
                    Swab endocervical;<br>
                    Swab de esfregaço vaginal;<br>
                    Swab orofaringe;<br>
