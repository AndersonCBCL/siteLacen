                    <p>
                        O Laboratório Central de Saúde Pública “Dr. Milton Bezerra Sobral” – LACEN/PE é subordinado à Secretaria Executiva de Vigilância em Saúde e Atenção Primária (SEVSAP) da Secretaria Estadual de Saúde de Pernambuco (SES/PE). Tem suas origens históricas no governo do Exmo. Sr. Dr. Barbosa Lima, em 1892, quando assumiu a direção dos serviços sanitários de Pernambuco o qual reformou a Inspetoria de Higiene, órgão da Diretoria de Obras do Estado, tornando-a independente, e dirigida pelo médico Rodolfo Galvão que criou o Laboratório de Análises Químicas e Pesquisas Microscópicas, o Instituto Vacinogênico e o Serviço de Demografia Sanitária.
                    </p>
                    <p>
                        Posteriormente, em 1905, o Governador Sigismundo Antônio Gonçalves, na gestão do Dr. Constâncio Pontual, fazendo uso de suas atribuições, através da Lei n° 736, de 10 de outubro, oficializou o regulamento para o Serviço de Higiene de Pernambuco, que diz no seu artigo primeiro: o Serviço de Higiene do Estado ficará a cargo de uma repartição denominada Inspetoria de Higiene Pública, sendo esta auxiliada por um Conselho de Salubridade e tendo como dependências, um Laboratório para Análises Químicas e Bromatológicas, um Instituto Vacinogênico e Bacteriológico e um Desinfectório.
                    </p>
                    <p>
                        Ao assumir a Direção-Geral dos Serviços Sanitários, em 1923, o Dr. Amaury de Medeiros, encontrou o Laboratório Bacteriológico, o Laboratório Químico e o Instituto Vacinogênico sem meios para funcionamento. Somente em 1931 foi reorganizado o Departamento de Saúde e Assistência, criado o Departamento de Saúde Pública, que tinha no organograma um Laboratório de Saúde Pública, sendo o referido Departamento subordinado à Secretaria do Interior e Justiça. Esse Laboratório de Saúde Pública permaneceu como unidade do Departamento de Saúde Pública da Secretaria de Saúde e Assistência Social, desmembrada da Secretaria do Interior e Justiça até 1971, quando pela Resolução n° 01/1972, do Conselho Deliberativo da Fundação de Saúde Amaury de Medeiros, recém-criada, subordinou o LACEN/PE ao Departamento de Laboratórios da referida Fundação.
                    </p>
                    <p>
                        Sendo assim, desde sua criação em 1972, o LACEN/PE assumiu responsabilidades em consequência direta da necessidade do público, e outras por designação do Ministério da Saúde. Hoje, funciona em prédio histórico, tombado pelo patrimônio público, no Centro do Recife/PE, e só em 2022 foi responsável por mais de 300.000 exames realizados, conforme dados do Gerenciador de Ambiente Laboratorial (GAL).
                    </p>
                    <p>
                        O LACEN/PE é Laboratório de Referência, único Laboratório de Saúde Pública do Estado de Pernambuco que realiza análises de alta complexidade. Desempenha ações para atendimento ao diagnóstico laboratorial para as Vigilâncias Epidemiológica, Sanitária e Ambiental, por meio da realização de exames e ensaios de alta complexidade nas áreas de Biologia Médica (Bacteriologia, Endemias, Imunologia e Virologia); Análises Ambientais (Toxicologia e Cianobactérias); Análises Bromatológicas de água para consumo humano, água de diálise e alimentos (Bromatologia); Controle da qualidade de medicamentos, cosméticos e saneantes (Medicamentos). Realiza também capacitações e treinamento de profissionais em conformidade com as diretrizes do Núcleo de Estudo e Pesquisa do LACEN/PE (NEPEL); realiza controle de qualidade para os diagnósticos de interesse à Vigilância em Saúde e descentralização de diagnósticos e ensaios de baixa complexidade para os laboratórios da Rede Pernambucana de Laboratórios (RPELAB).
