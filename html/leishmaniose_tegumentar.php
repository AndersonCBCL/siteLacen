            <h1 class="titulo_exame" >Leishmaniose Tegumentar</h1>

            <section class="conteudo">

                <h3>Metodologia:</h3>
                <p>
                    Parasitológico: visualização de formas amastigotas.
                </p>    
                <h3>Amostra biológica:</h3>
                <p>
                    Linfa de lesão cutânea ulcerada.
                </p>    
                <h3>recipiente/Conservação da Amostra:</h3>
                <p>
                    Lâmina lapidada para microscopia para o exame direto;<br>
                    Conservar a amostra protegida e em temperatura ambiente, ﬁxada em metanol.
            .
                </p>
                <h3>Período ideal de coleta:</h3>
                <p>
                    A partir da suspeita clínica ou inquéritos/monitoramentos epidemiológicos.
                </p>
                <h3>Técnica de Coleta:</h3>
                <p>
                    Retirar, se necessário, crostas que recobrem a lesão, empregando soro ﬁsiológico preferencialmente;<br>
                    Realizar antissepsia nas bordas da lesão, empregando uma compressa com álcool 70%. Passar somente nas bordas; <br>
                    Identificar as lâminas com iniciais, numeração das lâminas (1, 2, 3 e 4), data e diagnóstico; <br>
                    Utilizar lâmina de bisturi n° 15, 21 ou 25 ou alça graduada descartável, de acordo com o tamanho e localização da lesão, realizar leve escariﬁcação em vários pontos da lesão (no mínimo 3), procurando as regiões mais inﬁltradas (bordos mais elevados); <br>
                    No momento que houver sangramento, realizar compressão na área, evitando presença de sangue no esfregaço; <br>
                    Colher o material utilizando a parte cega do bisturi (costas do bisturi) e realizar pequenos esfregaços do material coletado em lâminas de microscopia, limpas, desengorduradas, secas e devidamente identiﬁcadas (3 lâminas por paciente); <br>
                    Não sobrepor os esfregaços; <br>
                    Após a secagem do esfregaço, corar as lâminas pelo método de Giemsa ou  coloração rápida (panótico). Caso a Unidade não disponha do corante, fixar a lâmina com metanol para conservação das estruturas.            
                </p>   
                <h3>Transporte:</h3>
                <p>
                    Transportar as lâminas em caixas apropriadas para transporte;<br>
                    Entregar as amostras na Recepção do LABEND/LACEN-PE,devidamente identiﬁcadas, cadastradas no sistema GAL, com as respectivas ﬁchas de investigação.
                </p>
                <h3>Formulários requeridos:</h3>
                <p>
                    Ficha de requisição do GAL e formulários específicos distribuídos pela CVLZOE.

                </p>
                <h3>Informações complementares:</h3>
                <p>
                    O diagnóstico depende, além da coleta e transporte adequados, de seu processamento correto antes do exame micológico. Necessário lâminas de boa qualidade para realização da leitura da mesma.
                </p>
