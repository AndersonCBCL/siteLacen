            <h1 class="titulo_exame" >Filariose Linfática</h1>

            <section class="conteudo">

                <h3>Metodologia:</h3>
                <p>
                    Parasitológico: pesquisa de microfilária por meio de microscopia da gota espessa de sangue.
                </p>    
                <h3>Amostra biológica/Volume Ideal</h3>
                <p>
                    Sangue capilar: punção digital - 3 gotas grandes de sangue (aproximadamente 60μL).
                </p>        
                <h3>Período ideal de coleta:</h3>
                <p>
                    A partir da suspeita clínica e epidemiológica, a partir das 23 horas.
                </p>
                <h3>Orientações para a coleta de amostra:</h3>
                <p>
                    <b>Técnica de coleta - Gota espessa:</b><br>
                    Obtenção da amostra sanguínea mediante a utilização de lanceta descartável;<br>
                    Recomenda-se que a punção seja realizada no dedo anelar preferencialmente da mão esquerda para os indivíduos destros e vice-versa; <br>
                    Proceder a limpeza do local a ser puncionado com algodão embebido em solução desinfetante (álcool 70%);<br>
                    A punção deve ser feita de forma rápida e precisa na borda lateral da extremidade digital e nunca diretamente na polpa digital; <br>
                    Deixe fluir o sangue, exercendo uma leve pressão sobre o dedo; <br>
                    Em caso de impedimento de se utilizar os dedos das mãos, poderá ser puncionado o lóbulo da orelha; <br>
                    Com a lâmina sobre uma superfície plana e nivelada, podem-se colocar três gotas grandes de sangue correspondendo a aproximadamente 60 μl; <br>
                    Deixar fluir a gota sobre a lâmina, não permitindo que a área puncionada entre em contato com a superfície da lâmina; <br>
                    Imediatamente as gotas de sangue devem ser espalhadas com a extremidade da própria lanceta utilizada para a punção de maneira que forme um retângulo homogêneo de bordas regulares; <br>
                    As lâminas contendo as amostras deverão permanecer protegidas dos insetos, em local plano e nivelado, seco e livre de poeira, até que o sangue esteja seco.<br>
                    <b>Técnica de coloração - Gota espessa:</b><br>
                    As lâminas com material (sangue) devem estar completamente secas;<br>
                    <u>Desemoglobinização</u>:<br>
                        Deixar as lâminas no mínimo 10 minutos em água destilada;<br>
                        Deixar secar;<br>
                        Fixar com álcool metílico 3 minutos ou etílico 20 minutos;<br>
                        Deixar secar.<br>
                    <u>Coloração</u>:<br>
                        Cobrir as lâminas com eosina amarela a 0,05% em água destilada por 5 minutos;<br>
                        Deixar secar;<br>
                        Contra corar: com Giemsa diluído na proporção de uma gota de corante para cada 01 ml de água tamponada a 6/4 por 30 minutos;<br>
                        Deixar secar.
                </p>
                <h3>Conservação da amostra até o envio:</h3>
                <p>
                    Lâmina lapidada para microscopia;<br>
                    Conservar a amostra protegida e em temperatura ambiente até serem enviadas ao laboratório.
                </p>
                <h3>Forma de acondicionamento para transporte:</h3>
                <p>
                    Em caixas apropriadas para transporte de lâminas, devidamente identificadas;<br>
                    Entregar na Recepção do LABEND/LACEN-PE;<br>
                    As amostras para Controle de Qualidade devem ser entregues na Recepção do LABEND/LACEN-PE, acompanhadas de planilha específica, totalmente preenchida.
                </p>
                <h3>Formulários requeridos:</h3>
                <p>
                    Ofício, com a planilha de resultados do diagnóstico devidamente preenchido;<br>
                    Quando se tratar de material para primeiro diagnóstico, enviar juntamente com as lâminas a Ficha de Investigação de Agravos de Notificação do SINAN devidamente preenchida.
                </p>
                <h3>Informações complementares:</h3>
                <p>
                    As amostras devem ser encaminhadas ao LACEN, cadastradas no Sistema de Ambiente Laboratorial (GAL);<br>
                    Os resultados são disponibilizados via GAL.
                </p>
