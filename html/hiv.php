            <h1 class="titulo_exame" >Vírus da Imunodeficiência Humana (HIV)</h1>

            <section class="conteudo">

                <h3>Metodologia:</h3>
                <P><b>Diagnóstico Sorológicos</b><br>
                    Sorologia: pesquisa de anticorpos por ensaios imunoenzimáticos/ quimioluminescência/ eletroquimioluminescência;<br>
                    Imunoblot rápido.<br>
                </p>
           
                <P><b>Diagnóstico Molecular</b><br>          
                    PCR em Tempo Real
                <P> 
                <h3>Amostra biológica:</h3>
                <p>
                    Soro - Sorologia;<br>
                    Plasma - Biologia Molecular: PCR em tempo real
                </p>    
                <h3>Volume ideal:</h3>
                <p>
                    Sangue venoso colhido em EDTA ou sem anticoagulante 5 ml.
                </p>
                <h3>Período ideal de coleta:</h3>
                <p>
                    Na suspeita clínica é solicitada uma coleta.
                </p>
                <h3>Orientações para a coleta de amostra:</h3>
                <p>
                    Soro: Coleta por punção venosa em tubo sem anticoagulante. Após a coleta realizar a centrifugação da amostra para separação do soro;<br>
                    Plasma: Coleta por punção venosa (mínimo de 5ml de sangue) em tubo com anticoagulante EDTA.
                </p>
                <h3>Conservação da amostra até o envio:</h3>
                <p>
                    Soro: acondicionado em tubo de polipropileno seco de 5 mL com tampa. Até o envio ao LACEN-PE, pode ser armazenado de 2ºC a 8ºC, no máximo, por 48 horas ;<br>
                    Plasma: acondicionado em tubos tipo Eppendorf ou tubos de ensaio tipo hemólise  de acrílico estéril, transferido com ponteiras estéreis ou com barreira. Até a separação do plasma, ele poderá ficar à temperatura ambiente (18ºC a 30ºC) por 06 horas.
                </p>
                <h3>Forma de acondicionamento para transporte:</h3>
                <p>
                    Caixas isotérmicas com gelo reciclável sinalizadas com símbolo de risco biológico;<br>
                    Usar suporte (estante) para conter derramamento ou vazamento durante o transporte e forrar a caixa isotérmica com papel absorvente;<br>
                    Entregar as amostras na Recepção de Amostras Biológicas do LACEN-PE.<br>
                    <b>Carga Viral:</b><br>
                    Acondicionar o plasma em tubos tipo eppendorfou em tubos de ensaio do tipo hemólise de plástico estéreis, que devem ser colocados em estante adequada para evitar o derramamento ou vazamento durante o transporte;<br>
                    Utilizar caixa isotérmica rígida para o material já separado, com gelo reciclável e símbolo de risco biológico;<br>
                    As amostras coletadas nas unidades de saúde situadas no Recife ou na Região Metropolitana, cujo transporte até o LACEN-PEocorra num intervalo inferior a 4 horas, poderão ser encaminhadas sem separação do plasma sem congelamento prévio em caixas isotérmicas com bateria reciclável;<br>
                    Não enviar amostras através de transportadoras ou companhia aérea em finais de semana ou em véspera de feriado.
                </p>
                <h3>Formulários requeridos:</h3>
                <p>
                    Todo material deve ser enviado ao LACEN-PEdevidamente identiﬁcado e acompanhado da ﬁcha de encaminhamento;<br>
                    É necessário enviar a Ficha Epidemiológica do sistema de requisição de exames de HIV- SIREX, devidamente preenchida e protegida do contato direto com as amostras;<br>
                    Enviar para cada amostra o BPA-I preenchido de forma adequada para carga viral.
                </p>
                <h3>Informações complementares:</h3>
                <p>
                    Não enviar amostras através de transportadoras ou companhia aérea em finais de semana ou em véspera de feriado;<br>
                    O recebimento de amostras para Carga Viral do HIV é feito de segunda a quinta-feira, de 07 horas às 14 horas;
                    Entregar as amostras na Recepção de Amostras Biológicas do LACEN-PE.
                </p>
