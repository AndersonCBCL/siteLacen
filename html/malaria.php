            <section class="conteudo">

                <h3>Metodologia:</h3>
                <p>
                    Parasitológico: pesquisa de Plasmodium por meio de microscopia da gota espessa de sangue corada pelo método de Giemsa;<br>
                    Teste rápido (TR) imunocromatográfico.
                </p>    
                <h3>Amostra biológica/Volume ideal:</h3>
                <p>
                    Sangue capilar (punção digital) uma gota de sangue (aproximadamente 20 μl);<br>
                    Sangue total coletado em tubo com EDTA.
                </p>
                <h3>Período ideal de coleta:</h3>
                <p>
                    A partir da suspeita clínica e epidemiológica.
                </p>
                <h3>Orientações para a coleta de amostra:</h3>
                <p>
                    <b>Técnica de coleta - Gota espessa:</b><br>
                    Separar duas lâminas limpas, deixando-as em superfície plana e horizontal;><br>
                    Identificar a lâmina com o número do registro do paciente;<br>
                    Limpar a pele do local da punção (parte lateral do segundo ou terceiro dedo da mão, lóbulo da orelha ou, em lactentes, o dedo grande do pé ou calcanhar) com gaze ou algodão embebido em álcool a 70%. Posteriormente enxugar com gaze ou algodão seco;<br>
                    Retirar o estilete (lanceta) do envoltório estéril, segurando- firmemente;<br>
                    Segurar o dedo a ser puncionado entre o polegar e o indicador da mão do operador e puncionar o local de maneira firme e leve;<br>
                    Remover a primeira gota de sangue com gaze ou algodão seco;<br>
                    Comprimir o dedo suavemente (como em ordenha) para obter outra gota de sangue esférica sobre a pele seca;<br>
                    Segurar a lâmina firmemente pelas bordas da extremidade onde se encontra a identificação. Aproximar a lâmina ao dedo do paciente até tocar o alto da gota de sangue (evitando o contato com a pele);<br>
                    Limpar o local puncionado com gaze ou algodão embebido em álcool a 70% e se necessário, pressioná-lo;<br>
                    Deixar a lâmina secar em temperatura ambiente e enviá-la ao laboratório para que seja corada.<br>
                    <b>Técnica de coleta - Esfregaço distendido:</b><br>
                    Separar duas lâminas limpas, deixando-as em superfície plana e horizontal;<br>
                    Identificar a lâmina com o número do registro do paciente;<br>
                    Limpar a pele do local de punção (parte lateral do segundo ou terceiro dedo da mão, lóbulo da orelha ou, em lactentes, o dedo grande do pé ou calcanhar) com gaze ou algodão embebido em álcool a 70%; posteriormente, enxugar com gaze ou algodão secos;<br>
                    Retirar o estilete (lanceta) do envoltório estéril, segurando- o firmemente;<br>
                    Segurar o dedo a ser puncionado entre o polegar e o indicador da mão do operador e puncionar o local de maneira firme e leve;<br>
                    Remover a primeira gota de sangue com gaze ou algodão seco;<br>
                    Comprimir o dedo suavemente (como em ordenha) para obter outra pequena gota de sangue esférica sobre a pele seca. Cuidar para não tocar o ponto de saída do sangue;<br>
                    Segurar a lâmina firmemente pelas bordas da extremidade onde se encontra a identificação e aproximar a lâmina ao dedo do paciente até tocar o alto da gota de sangue (evitando o contato com a pele);<br>
                    Limpar o local puncionado com gaze ou algodão embebido em álcool a 70% e se necessário, pressioná-lo;<br>
                    Com a borda estreita da lâmina biselada em contato com a gota de sangue, formando um ângulo de 50º, espalhar o sangue com um movimento rápido para formar uma camada delgada (se possível, uma única camada de células), sem atingir a outra extremidade da lâmina;<br>
                    Deixar secar em temperatura ambiente na posição horizontal e enviar ao laboratório para análise.
                </p>
                <h3>Conservação da amostra até o envio:</h3>
                <p>
                    Lâmina lapidada para microscopia;<br>
                    Conservar a amostra protegida e em temperatura ambiente até serem enviadas ao laboratório;<br>
                    Sangue total: conservar sob refrigeração.
                </p>
                <h3>Forma de acondicionamento para transporte:</h3>
                <p>
                    Em caixas apropriadas para transporte de lâminas, devidamente identificadas.<br>
                    As amostras devem estar cadastradas no GAL e acompanhadas da Ficha de Investigação de Agravos de Notificação do SINAN devidamente preenchida;<br>
                    Tubo de coleta com EDTA devidamente identificado;<br>
                    As amostras para Controle de Qualidade devem ser entregues na Recepção do LABEND/LACEN-PE, acompanhadas de planilha específica, totalmente preenchida.
                </p>
                <h3>Formulários requeridos:</h3>
                <p>
                    Obrigatório o cadastro no Gerenciador de Ambiente Laboratorial (GAL) e notificação no SINAN. 
                </p>
                <h3>Informações complementares:</h3>
                <p>
                    As amostras devem ser encaminhadas ao LACEN, cadastradas no Sistema Gerenciador de Ambiente Laboratorial (GAL).<br>
                    Os resultados são disponibilizados via GAL.
                </p>
