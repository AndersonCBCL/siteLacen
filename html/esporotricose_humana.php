                <h3>Metodologia:</h3>
                <p>
                    Exame direto: visualização da forma leveduriforme.
                </p>    
                <h3>Amostra biológica:</h3>
                <p>
                    Secreção de lesão.
                </p>
                <h3>Período da coleta:</h3>
                <p>
                    A partir da suspeita clínica, de preferência sem nenhum tratamento prévio.
                </p>
                <h3>Material necessário para coleta:</h3>
                <p>
                    Lâmina lapidada para microscopia para o exame direto.
                </p>
                <h3>Conservação da amostra:</h3>
                <p>
                    Lâmina lapidada para microscopia: Conservar a amostra protegida e em temperatura ambiente por até 24 horas.
                </p>
                <h3>Orientações para a coleta de amostra:</h3>
                    Nos 15 dias que precedem a coleta, deve-se suspender a aplicação de antifúngicos orais e tópicos;<br>
                    Coletar de duas a quatro lâminas para o exame direto, preferencialmente de uma única lesão;<br>
                    No caso de abscessos fechados a obtenção de material purulento é feita por punção, com seringa esterilizada ou por drenagem com prévia limpeza da zona implicada. Apenas profissionais capacitados deverão realizar a punção; <br>
                    Quando o abscesso drena espontaneamente, o material purulento deve ser colhido com espátula, alça, bisturi ou swab e então transferido diretamente para o meio de cultura (semeio direto); <br>
                    Já em lesão aberta deve ser feita a escarificação nas bordas com auxílio de bisturi, uma vez que é provável que se encontre estruturas fúngicas viáveis nesta área; <br>
                    Realizar, sempre que possível, o semeio direto do material purulento obtido (de linfonodos ou de lesões únicas) em placas de Petri com o meio, maximizando assim as chances de crescimento fúngico. Para isso, inocular o material em sete pontos mais ou menos equidistantes da placa; <br>
                    Identificar corretamente lâminas e placas; <br>
                    Coletar, pelo menos, duas placas por paciente (para a incubação, dar preferência à temperatura ambiente).
                <h3>Transporte:</h3>
                <p>
                    Após confecção das lâminas, introduzir o swab em tubo de ensaio estéril, com cerca de 1 mL de soro fisiológico (NaCl 0,9%), fechando com uma bola de algodão. Alternativamente, pode-se introduzir o swab em meio Cary Blair;<br>
                    Enviar para o laboratório no mesmo dia, para que seja iniciado o procedimento de isolamento;<br>
                    Recomenda-se não refrigerar ou congelar a amostra; <br>
                    Lâminas devem ser acondicionadas à temperatura ambiente em caixas apropriadas com as fichas de investigação; <br>
                    Entregar as amostras (lâminas e swab) na Recepção do LABEND, já cadastradas no Sistema GAL e acompanhadas de formulário específico distribuído pela CVLZOE <b>QUAL FORMULARIO?</b>
                </p>
                <h3>Formulários requeridos:</h3>
                <p>
                    Ficha de requisição do GAL e formulários específicos distribuídos pela CVLZOE. 
                </p>
                <h3>Informações complementares:</h3>
                <p>
                    Coletar as amostras preferencialmente antes do uso de antifúngicos.
                </p>
